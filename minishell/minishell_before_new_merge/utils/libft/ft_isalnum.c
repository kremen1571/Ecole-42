#include "libft.h"

int	ft_isalnum(int c)
{
	int value;

	if ((c >= 48 && c <= 57) || (c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		value = 8;
	else
		value = 0;
	return (value);
}
