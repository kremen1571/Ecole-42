#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;

	ptr = malloc(size * nmemb);
	if (ptr)
		return (ft_memset(ptr, 0, nmemb * size));
	return (NULL);
}
